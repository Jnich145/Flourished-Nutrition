export const getUniqueTags = (meals: Meal[]): string[] => {
  return Array.from(new Set(meals.flatMap(meal => meal.tags))).sort();
};

export const filterMeals = (meals: Meal[], searchTerm: string, selectedTag: string | null): Meal[] => {
  return meals.filter((meal) => {
    const matchesSearch = meal.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
      meal.description.toLowerCase().includes(searchTerm.toLowerCase());
    const matchesTag = !selectedTag || meal.tags.includes(selectedTag);
    return matchesSearch && matchesTag;
  });
};